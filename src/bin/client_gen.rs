// src/bin/gen_clients.rs
// Generates multiple client Rust files into src/bin/
//
// Usage:
//   cargo run --bin gen_clients
//
// It will write:
//   - src/bin/client_test1.rs  (1 auth per run)
//   - src/bin/client_test2.rs  (50 auth iterations per run)
//   - src/bin/client_test3.rs  (50 auth iterations per run; intended for 50 concurrent clients)

use std::fs;
use std::io;
use std::path::Path;

fn write_file(path: &str, contents: &str) -> io::Result<()> {
    let p = Path::new(path);
    if let Some(parent) = p.parent() {
        fs::create_dir_all(parent)?;
    }
    fs::write(p, contents)?;
    println!("Wrote {}", path);
    Ok(())
}

// --- Common client template: your original client.rs with two customizable knobs:
// 1) TAG: "T1"/"T2"/"T3" (log prefix only)
// 2) MODE:
//    - AUTH_ONCE: run one do_auth_once
//    - AUTH_LOOP: run N iterations in a loop

fn client_template(tag: &str, auth_iterations: usize, loop_mode: bool) -> String {
    // Notes:
    // - This template includes --state-dir support and --setup support.
    // - For T1: loop_mode=false
    // - For T2/T3: loop_mode=true and auth_iterations=50
    format!(
r#"// AUTO-GENERATED by gen_clients.rs
// {tag}: Client variant for experiment runs.
//
// Build/run examples:
//   cargo run --release --bin client_test1 -- --server 127.0.0.1:4000 --state-dir clients/c01 --setup
//   cargo run --release --bin client_test1 -- --server 127.0.0.1:4000 --state-dir clients/c01
//
// Flags:
//   --server <ip:port>
//   --state-dir <dir>
//   --setup

use std::env;
use std::fs;
use std::io::{{Read, Write}};
use std::net::TcpStream;
use std::path::{{Path, PathBuf}};
use std::time::{{Duration, Instant}};

use curve25519_dalek::constants::RISTRETTO_BASEPOINT_POINT;
use curve25519_dalek::ristretto::{{CompressedRistretto, RistrettoPoint}};
use curve25519_dalek::scalar::Scalar;
use hkdf::Hkdf;
use merlin::Transcript;
use rand::{{rngs::OsRng, RngCore}};
use sha2::Sha256;
use zeroize::Zeroize;

const MSG_SETUP: u8 = 0x01;
const MSG_AUTH: u8 = 0x02;

const DEVICE_ID_LEN: usize = 32;
const NONCE_LEN: usize = 32;

// Demo constant server identity binding.
static SERVER_ID: [u8; 32] = [0x53u8; 32];

const AUTH_ITERATIONS: usize = {auth_iterations};

// -------------------- path helpers --------------------

fn device_id_path(state_dir: &Path) -> PathBuf {{ state_dir.join("device_id.bin") }}
fn device_x_path(state_dir: &Path) -> PathBuf {{ state_dir.join("device_x.bin") }}

fn ensure_state_dir(state_dir: &Path) -> std::io::Result<()> {{
    if !state_dir.exists() {{
        fs::create_dir_all(state_dir)?;
    }}
    Ok(())
}}

fn creds_exist(state_dir: &Path) -> bool {{
    device_id_path(state_dir).exists() && device_x_path(state_dir).exists()
}}

// -------------------- crypto helpers --------------------

fn random_scalar() -> Scalar {{
    let mut bytes = [0u8; 64];
    OsRng.fill_bytes(&mut bytes);
    Scalar::from_bytes_mod_order_wide(&bytes)
}}

fn random_bytes_32() -> [u8; 32] {{
    let mut b = [0u8; 32];
    OsRng.fill_bytes(&mut b);
    b
}}

fn schnorr_prove_setup(x: &Scalar, device_id: &[u8; 32], server_nonce: &[u8; 32]) -> (RistrettoPoint, Scalar) {{
    let pubkey = RISTRETTO_BASEPOINT_POINT * x;
    let r = random_scalar();
    let a = RISTRETTO_BASEPOINT_POINT * r;

    let mut t = Transcript::new(b"setup_schnorr");
    t.append_message(b"device_id", device_id);
    t.append_message(b"pubkey", pubkey.compress().as_bytes());
    t.append_message(b"a", a.compress().as_bytes());
    t.append_message(b"server_nonce", server_nonce);

    let mut buf = [0u8; 64];
    t.challenge_bytes(b"c", &mut buf);
    let c = Scalar::from_bytes_mod_order_wide(&buf);

    let s = r + c * x;
    (a, s)
}}

fn schnorr_prove_auth(
    x: &Scalar,
    device_id: &[u8; 32],
    nonce_c: &[u8; 32],
    eph_c: &RistrettoPoint,
    label: &'static [u8],
) -> (RistrettoPoint, Scalar) {{
    let pubkey = RISTRETTO_BASEPOINT_POINT * x;
    let r = random_scalar();
    let a = RISTRETTO_BASEPOINT_POINT * r;

    let mut t = Transcript::new(label);
    t.append_message(b"device_id", device_id);
    t.append_message(b"pubkey", pubkey.compress().as_bytes());
    t.append_message(b"a", a.compress().as_bytes());
    t.append_message(b"nonce_c", nonce_c);
    t.append_message(b"eph_c", eph_c.compress().as_bytes());

    let mut buf = [0u8; 64];
    t.challenge_bytes(b"c", &mut buf);
    let c = Scalar::from_bytes_mod_order_wide(&buf);

    let s = r + c * x;
    (a, s)
}}

fn schnorr_verify_server(
    server_static_pub: &RistrettoPoint,
    a: &RistrettoPoint,
    s: &Scalar,
    server_id: &[u8; 32],
    nonce_s: &[u8; 32],
    eph_s: &RistrettoPoint,
    label: &'static [u8],
) -> bool {{
    let mut t = Transcript::new(label);
    t.append_message(b"server_id", server_id);
    t.append_message(b"pubkey", server_static_pub.compress().as_bytes());
    t.append_message(b"a", a.compress().as_bytes());
    t.append_message(b"nonce_s", nonce_s);
    t.append_message(b"eph_s", eph_s.compress().as_bytes());

    let mut buf = [0u8; 64];
    t.challenge_bytes(b"c", &mut buf);
    let c = Scalar::from_bytes_mod_order_wide(&buf);

    RISTRETTO_BASEPOINT_POINT * s == a + server_static_pub * c
}}

fn derive_session_key(
    eph_secret: &Scalar,
    peer_eph_pub: &RistrettoPoint,
    nonce_c: &[u8; 32],
    nonce_s: &[u8; 32],
    device_id: &[u8; 32],
    eph_c: &RistrettoPoint,
    eph_s: &RistrettoPoint,
) -> [u8; 32] {{
    let shared = peer_eph_pub * eph_secret;
    let shared_bytes = shared.compress().to_bytes();

    let mut salt = [0u8; 64];
    salt[..32].copy_from_slice(nonce_c);
    salt[32..].copy_from_slice(nonce_s);

    let mut info = Vec::with_capacity(10 + 32 + 32 + 32);
    info.extend_from_slice(b"session key");
    info.extend_from_slice(device_id);
    info.extend_from_slice(eph_c.compress().as_bytes());
    info.extend_from_slice(eph_s.compress().as_bytes());

    let hk = Hkdf::<Sha256>::new(Some(&salt), &shared_bytes);
    let mut okm = [0u8; 32];
    hk.expand(&info, &mut okm).unwrap();
    okm
}}

// -------------------- IO helpers with byte counting --------------------

fn send_all(stream: &mut impl Write, buf: &[u8], sent: &mut usize) -> std::io::Result<()> {{
    *sent += buf.len();
    stream.write_all(buf)
}}

fn recv_exact(stream: &mut impl Read, buf: &mut [u8], recv: &mut usize) -> std::io::Result<()> {{
    stream.read_exact(buf)?;
    *recv += buf.len();
    Ok(())
}}

fn recv_point(stream: &mut impl Read, recv: &mut usize) -> std::io::Result<RistrettoPoint> {{
    let mut b = [0u8; 32];
    recv_exact(stream, &mut b, recv)?;
    CompressedRistretto(b)
        .decompress()
        .ok_or_else(|| std::io::Error::new(std::io::ErrorKind::InvalidData, "invalid point"))
}}

fn recv_scalar(stream: &mut impl Read, recv: &mut usize) -> std::io::Result<Scalar> {{
    let mut b = [0u8; 32];
    recv_exact(stream, &mut b, recv)?;
    let ct = Scalar::from_canonical_bytes(b);
    if ct.is_some().unwrap_u8() == 1 {{ Ok(ct.unwrap()) }}
    else {{ Err(std::io::Error::new(std::io::ErrorKind::InvalidData, "invalid scalar")) }}
}}

// -------------------- credential storage --------------------

fn load_device_creds(state_dir: &Path) -> std::io::Result<([u8; 32], Scalar)> {{
    let id_bytes = fs::read(device_id_path(state_dir))?;
    if id_bytes.len() != DEVICE_ID_LEN {{
        return Err(std::io::Error::new(std::io::ErrorKind::InvalidData, "device_id wrong length"));
    }}
    let mut device_id = [0u8; 32];
    device_id.copy_from_slice(&id_bytes);

    let x_bytes = fs::read(device_x_path(state_dir))?;
    if x_bytes.len() != 32 {{
        return Err(std::io::Error::new(std::io::ErrorKind::InvalidData, "device_x wrong length"));
    }}
    let mut xb = [0u8; 32];
    xb.copy_from_slice(&x_bytes);

    let ct = Scalar::from_canonical_bytes(xb);
    if ct.is_some().unwrap_u8() != 1 {{
        return Err(std::io::Error::new(std::io::ErrorKind::InvalidData, "device_x not canonical scalar"));
    }}
    Ok((device_id, ct.unwrap()))
}}

fn save_device_creds(state_dir: &Path, device_id: &[u8; 32], x: &Scalar) -> std::io::Result<()> {{
    ensure_state_dir(state_dir)?;
    fs::write(device_id_path(state_dir), device_id)?;
    fs::write(device_x_path(state_dir), x.to_bytes())?;
    Ok(())
}}

// -------------------- SETUP --------------------

fn do_setup(server_addr: &str, state_dir: &Path, device_id: [u8; 32], mut x: Scalar) -> std::io::Result<()> {{
    let mut sent = 0usize;
    let mut recv = 0usize;

    let mut stream = TcpStream::connect(server_addr)?;
    println!("Client[{tag}][SETUP]: Connected to {{}}", server_addr);

    send_all(&mut stream, &[MSG_SETUP], &mut sent)?;
    send_all(&mut stream, &[0u8], &mut sent)?; // token_len = 0

    let static_pub = RISTRETTO_BASEPOINT_POINT * x;
    send_all(&mut stream, &device_id, &mut sent)?;
    send_all(&mut stream, static_pub.compress().as_bytes(), &mut sent)?;
    stream.flush()?;

    let mut server_nonce = [0u8; 32];
    recv_exact(&mut stream, &mut server_nonce, &mut recv)?;

    let (a, s) = schnorr_prove_setup(&x, &device_id, &server_nonce);
    send_all(&mut stream, a.compress().as_bytes(), &mut sent)?;
    send_all(&mut stream, &s.to_bytes(), &mut sent)?;
    stream.flush()?;

    save_device_creds(state_dir, &device_id, &x)?;
    println!("Client[{tag}][SETUP]: Sent={{}} bytes, Received={{}} bytes, device_id={{}}",
        sent, recv, hex::encode(device_id));

    x.zeroize();
    Ok(())
}}

// -------------------- AUTH (single) --------------------

fn do_auth_once(server_addr: &str, device_id: [u8; 32], mut x: Scalar) -> std::io::Result<(Duration, usize, usize)> {{
    let start = Instant::now();
    let mut sent = 0usize;
    let mut recv = 0usize;

    let mut stream = TcpStream::connect(server_addr)?;

    let mut nonce_c = [0u8; NONCE_LEN];
    OsRng.fill_bytes(&mut nonce_c);

    let mut eph_secret = random_scalar();
    let eph_pub = RISTRETTO_BASEPOINT_POINT * eph_secret;

    let (a, s) = schnorr_prove_auth(&x, &device_id, &nonce_c, &eph_pub, b"client_schnorr");

    send_all(&mut stream, &[MSG_AUTH], &mut sent)?;
    send_all(&mut stream, &device_id, &mut sent)?;
    send_all(&mut stream, a.compress().as_bytes(), &mut sent)?;
    send_all(&mut stream, &s.to_bytes(), &mut sent)?;
    send_all(&mut stream, &nonce_c, &mut sent)?;
    send_all(&mut stream, eph_pub.compress().as_bytes(), &mut sent)?;
    stream.flush()?;

    let server_static_pub = recv_point(&mut stream, &mut recv)?;
    let server_a = recv_point(&mut stream, &mut recv)?;
    let server_s = recv_scalar(&mut stream, &mut recv)?;
    let mut nonce_s = [0u8; NONCE_LEN];
    recv_exact(&mut stream, &mut nonce_s, &mut recv)?;
    let eph_s = recv_point(&mut stream, &mut recv)?;

    let ok = schnorr_verify_server(&server_static_pub, &server_a, &server_s, &SERVER_ID, &nonce_s, &eph_s, b"server_schnorr");
    if !ok {{
        x.zeroize();
        eph_secret.zeroize();
        return Err(std::io::Error::new(std::io::ErrorKind::Other, "server authentication failed"));
    }}

    let _key = derive_session_key(&eph_secret, &eph_s, &nonce_c, &nonce_s, &device_id, &eph_pub, &eph_s);

    x.zeroize();
    eph_secret.zeroize();

    Ok((start.elapsed(), sent, recv))
}}

fn do_auth_loop(server_addr: &str, device_id: [u8; 32], x: Scalar) -> std::io::Result<()> {{
    println!("Client[{tag}][AUTH]: Running {{}} iterations over TCP to {{}}", AUTH_ITERATIONS, server_addr);

    let mut total_time = Duration::ZERO;
    let mut total_sent = 0usize;
    let mut total_recv = 0usize;

    let mut min_time: Option<Duration> = None;
    let mut max_time: Option<Duration> = None;

    for i in 0..AUTH_ITERATIONS {{
        let (dur, sent, recv) = do_auth_once(server_addr, device_id, x)?;
        total_time += dur;
        total_sent += sent;
        total_recv += recv;

        min_time = Some(min_time.map_or(dur, |m| m.min(dur)));
        max_time = Some(max_time.map_or(dur, |m| m.max(dur)));

        println!("  Iteration {{:02}}: Duration={{:?}}, Sent={{}} bytes, Received={{}} bytes", i+1, dur, sent, recv);
    }}

    let avg_time = total_time / (AUTH_ITERATIONS as u32);
    let avg_sent = total_sent / AUTH_ITERATIONS;
    let avg_recv = total_recv / AUTH_ITERATIONS;

    println!("\nCLIENT SUMMARY ({tag}, {{}} iterations)", AUTH_ITERATIONS);
    println!("  Avg Duration : {{:?}}", avg_time);
    println!("  Min Duration : {{:?}}", min_time.unwrap());
    println!("  Max Duration : {{:?}}", max_time.unwrap());
    println!("  Avg Sent     : {{}} bytes", avg_sent);
    println!("  Avg Received : {{}} bytes", avg_recv);
    println!("  Avg RTT Bytes: {{}} bytes", avg_sent + avg_recv);

    Ok(())
}}

// -------------------- MAIN --------------------

fn main() -> std::io::Result<()> {{
    let args: Vec<String> = env::args().collect();

    let mut server_addr = "127.0.0.1:4000".to_string();
    let mut do_setup_flag = false;
    let mut state_dir = PathBuf::from(".");

    let mut i = 1;
    while i < args.len() {{
        match args[i].as_str() {{
            "--server" => {{
                if i + 1 >= args.len() {{
                    return Err(std::io::Error::new(std::io::ErrorKind::InvalidInput, "--server missing value"));
                }}
                server_addr = args[i + 1].clone();
                i += 2;
            }}
            "--setup" => {{
                do_setup_flag = true;
                i += 1;
            }}
            "--state-dir" => {{
                if i + 1 >= args.len() {{
                    return Err(std::io::Error::new(std::io::ErrorKind::InvalidInput, "--state-dir missing value"));
                }}
                state_dir = PathBuf::from(&args[i + 1]);
                i += 2;
            }}
            _ => {{
                return Err(std::io::Error::new(std::io::ErrorKind::InvalidInput, "unknown argument"));
            }}
        }}
    }}

    ensure_state_dir(&state_dir)?;

    if do_setup_flag {{
        let (device_id, x) = if creds_exist(&state_dir) {{
            println!("Client[{tag}][SETUP]: Using existing creds (idempotent).");
            load_device_creds(&state_dir)?
        }} else {{
            println!("Client[{tag}][SETUP]: Generating NEW device identity.");
            let device_id = random_bytes_32();
            let x = random_scalar();
            save_device_creds(&state_dir, &device_id, &x)?;
            (device_id, x)
        }};
        return do_setup(&server_addr, &state_dir, device_id, x);
    }}

    if !creds_exist(&state_dir) {{
        eprintln!("Client[{tag}]: creds missing in --state-dir. Run with --setup first.");
        return Ok(());
    }}

    let (device_id, x) = load_device_creds(&state_dir)?;

    if {loop_mode} {{
        do_auth_loop(&server_addr, device_id, x)
    }} else {{
        let (dur, sent, recv) = do_auth_once(&server_addr, device_id, x)?;
        println!("CLIENT METRICS ({tag}) -> Duration: {{:?}}, Sent: {{}} bytes, Received: {{}} bytes", dur, sent, recv);
        Ok(())
    }}
}}
"#,
        tag = tag,
        auth_iterations = auth_iterations,
        loop_mode = if loop_mode { "true" } else { "false" },
    )
}

fn main() -> io::Result<()> {
    // Test1: one auth per run
    let t1 = client_template("T1", 1, false);
    // Test2: 50 iterations from one client
    let t2 = client_template("T2", 50, true);
    // Test3: 50 iterations per client (intended for 50 concurrent clients)
    let t3 = client_template("T3", 50, true);

    write_file("src/bin/client_test1.rs", &t1)?;
    write_file("src/bin/client_test2.rs", &t2)?;
    write_file("src/bin/client_test3.rs", &t3)?;

    println!("Done. Now run via:");
    println!("  cargo run --release --bin client_test1 -- --server 127.0.0.1:4000 --state-dir clients/c01 --setup");
    println!("  cargo run --release --bin client_test2 -- --server 127.0.0.1:4000 --state-dir clients/c01");
    println!("  cargo run --release --bin client_test3 -- --server 127.0.0.1:4000 --state-dir clients/c01");
    Ok(())
}
